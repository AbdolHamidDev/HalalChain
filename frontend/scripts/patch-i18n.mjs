// ─── Deep merge helper ────────────────────────────────────────────────────────
function deepMerge(target, source) {
  const result = { ...target };
  for (const [key, value] of Object.entries(source)) {
    if (value && typeof value === "object" && !Array.isArray(value)) {
      result[key] = deepMerge(result[key] ?? {}, value);
    } else {
      // Only add if the key doesn't already exist
      if (!(key in result)) {
        result[key] = value;
      }
    }
  }
  return result;
}

// ─── Apply patches ────────────────────────────────────────────────────────────
for (const locale of ["en", "vi"]) {
  const filePath = path.join(LOCALES, `${locale}.json`);
  const existing = JSON.parse(fs.readFileSync(filePath, "utf8"));
  const patched = deepMerge(existing, patches[locale]);
  fs.writeFileSync(filePath, JSON.stringify(patched, null, 2) + "\n", "utf8");
  console.log(`✅ Patched ${locale}.json`);
}
